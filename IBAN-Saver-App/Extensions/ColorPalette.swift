import SwiftUI
import UIKit

extension UIColor {
    
    static var IBANBackground: UIColor {
        UIColor(Color.ibanBackground)
    }

    static var IBANButton: UIColor {
        return UIColor(Color.ibanButton)
    }
    
    static var IBANHeader: UIColor {
        return UIColor(Color.ibanHeader)
    }
    
    static var IBANText: UIColor {
        return UIColor(Color.ibanText)
    }
    
}
